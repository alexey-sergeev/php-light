<h2>Весна</h2>

<br />

<p><strong>Весна</strong> — одно из четырёх времён года, переходный сезон между <a href="?page=1">зимой</a> и <a href="?page=3">летом</a>. Это переходный сезон, 
когда заметно увеличение светового дня, повышается температура окружающей среды и активизируется природная деятельность живых существ и растений. 

<p>Астрономическая весна наступает во время весеннего равноденствия и продолжается до летнего солнцестояния. 

<p>Календарная весна состоит из трёх месяцев: в Северном полушарии — марта, апреля и мая, в Южном — сентября, октября и ноября.

